package pojo;

public class Role {
	private Integer roleId;
	private String roleName;
	private String rolePassword;
	private String roleRemark;
	private Integer roleTel;
	public Integer getRoleId() {
		return roleId;
	}
	public void setRoleId(Integer roleId) {
		this.roleId = roleId;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public String getRolePassword() {
		return rolePassword;
	}
	public void setRolePassword(String rolePassword) {
		this.rolePassword = rolePassword;
	}
	public String getRoleRemark() {
		return roleRemark;
	}
	public void setRoleRemark(String roleRemark) {
		this.roleRemark = roleRemark;
	}
	public Integer getRoleTel() {
		return roleTel;
	}
	public void setRoleTel(Integer roleTel) {
		this.roleTel = roleTel;
	}
	
}
